package yanjun.model;

public class Fabric {

	private int fabricId;
	private String fabricName;
	private String storePath;
	private float fabricPrice;
	
	public int getFabricId() {
		return fabricId;
	}
	public void setFabricId(int fabricId) {
		this.fabricId = fabricId;
	}
	public String getFabricName() {
		return fabricName;
	}
	public void setFabricName(String fabricName) {
		this.fabricName = fabricName;
	}
	public float getFabricPrice() {
		return fabricPrice;
	}
	public void setFabricPrice(float fabricPrice) {
		this.fabricPrice = fabricPrice;
	}
	public String getStorePath() {
		return storePath;
	}
	public void setStorePath(String storePath) {
		this.storePath = storePath;
	}
	
	
}
